/**
 * @description SpEL运算符--逻辑运算
 */
package com.spring.spel.oper;

public class SpelLogic {
	private boolean and;
	private boolean or;
	private boolean not;
	
	public boolean isAnd() {
		return and;
	}
	public void setAnd(boolean and) {
		this.and = and;
	}
	public boolean isOr() {
		return or;
	}
	public void setOr(boolean or) {
		this.or = or;
	}
	public boolean isNot() {
		return not;
	}
	public void setNot(boolean not) {
		this.not = not;
	}
	
}
