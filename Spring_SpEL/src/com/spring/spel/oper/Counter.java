/**
 * @description 依赖注入类，为SpelMath提供运算数值
 */
package com.spring.spel.oper;

public class Counter {
	private float total;
	private float count;
	
	public float getTotal() {
		return total;
	}
	public void setTotal(float total) {
		this.total = total;
	}
	public float getCount() {
		return count;
	}
	public void setCount(float count) {
		this.count = count;
	}
	
}
